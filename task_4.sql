SELECT 
    COLUMN_NAME AS 'COLUMN',
    COLUMN_TYPE AS 'TYPE',
    IS_NULLABLE AS 'NULLABLE',
    COLUMN_KEY AS 'KEY',
    COLUMN_DEFAULT AS 'DEFAULT',
    EXTRA AS 'EXTRA'
FROM 
    INFORMATION_SCHEMA.COLUMNS
WHERE 
    TABLE_SCHEMA = DATABASE()
    AND TABLE_NAME = 'books'
ORDER BY 
    ORDINAL_POSITION;
